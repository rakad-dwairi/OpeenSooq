/**
 * OpenWeather controller
 */
class OpenWeatherController extends BaseController
{
    public function actionGetCityWeather($city)
    {
        $curl = curl_init();

        curl_setopt_array($curl, array(
        CURLOPT_URL => Yii::$app->params['openWeatherUrl'] . '?q=' . $city . '&units=metric&lang=en&appid=' . Yii::$app->params['open_weather_app_key'],
        CURLOPT_RETURNTRANSFER => true,
        CURLOPT_ENCODING => '',
        CURLOPT_MAXREDIRS => 10,
        CURLOPT_TIMEOUT => 0,
        CURLOPT_FOLLOWLOCATION => true,
        CURLOPT_HTTP_VERSION => CURL_HTTP_VERSION_1_1,
        CURLOPT_CUSTOMREQUEST => 'GET',
        ));

        $response = curl_exec($curl);

        curl_close($curl);
        echo $response;
    }

   
}
